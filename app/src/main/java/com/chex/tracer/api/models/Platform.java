package com.chex.tracer.api.models;

public class Platform {
    private int id;
    private int name;

    public Platform() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
