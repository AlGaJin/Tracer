package com.chex.tracer.api.models;

public class Platform {
    private int id;
    private String name;

    public Platform() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
